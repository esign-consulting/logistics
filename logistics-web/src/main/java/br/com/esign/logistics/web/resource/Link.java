package br.com.esign.logistics.web.resource;

import java.util.Objects;

/**
 *
 * @author gustavomunizdocarmo
 */
public class Link {
    
    private final String rel;
    private final String href;

    public Link(String rel, String href) {
        this.rel = rel;
        this.href = href;
    }

    public String getRel() {
        return rel;
    }

    public String getHref() {
        return href;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 47 * hash + Objects.hashCode(this.rel);
        hash = 47 * hash + Objects.hashCode(this.href);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Link other = (Link) obj;
        if (!Objects.equals(this.rel, other.rel)) {
            return false;
        }
        return Objects.equals(this.href, other.href);
    }
    
}
